""" Write a program print n even number

        eg. n=5
        output = 2,4,6,8,10(totaly 5 even numbers are printing) """

n = int(input("Enter n = "))

for i in range(1,n+n+1) :

    if i % 2 == 0 :
        print("Display n even numbers = ",i)

print("Totally n even numbers are printing = ",n)










